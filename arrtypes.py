# One-dimensional array
numbers = [1, 2, 3, 4, 5]
print("One-dimensional array:", numbers)

# Accessing elements
print("First element:", numbers[0])
print("Last element:", numbers[-1])

# Multi-dimensional array (2D array)
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]
print("Multi-dimensional array (2D array):")
for row in matrix:
    print(row)

